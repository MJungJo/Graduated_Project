def category_page(request, slug):
    ## 미분류의 경유
    if slug == 'no_category':
        category = '미분류'
        post_list = Post.objects.filter(category=None)
    ## 미분류 외
    else:
        category = Category.objects.get(slug=slug)
        post_list = Post.objects.filter(category=category)

